package entity;

public class AdminEntity {
	private int idAdmin;
	private String nameAdmin;
	private String addressAdmin;
	private String telAdmin;
	private String emailAdmin;
	private int disable;
	private String passAdmin;

	public int getIdAdmin() {
		return idAdmin;
	}

	public void setIdAdmin(int idAdmin) {
		this.idAdmin = idAdmin;
	}

	public String getNameAdmin() {
		return nameAdmin;
	}

	public void setNameAdmin(String nameAdmin) {
		this.nameAdmin = nameAdmin;
	}

	public String getAddressAdmin() {
		return addressAdmin;
	}

	public void setAddressAdmin(String addressAdmin) {
		this.addressAdmin = addressAdmin;
	}

	public String getTelAdmin() {
		return telAdmin;
	}

	public void setTelAdmin(String telAdmin) {
		this.telAdmin = telAdmin;
	}

	public String getEmailAdmin() {
		return emailAdmin;
	}

	public void setEmailAdmin(String emailAdmin) {
		this.emailAdmin = emailAdmin;
	}

	public int getDisable() {
		return disable;
	}

	public void setDisable(int disable) {
		this.disable = disable;
	}

	public String getPassAdmin() {
		return passAdmin;
	}

	public void setPassAdmin(String passAdmin) {
		this.passAdmin = passAdmin;
	}

}
